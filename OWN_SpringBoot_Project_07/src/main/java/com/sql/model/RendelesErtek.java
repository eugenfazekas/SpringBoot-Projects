package com.sql.model;

public class RendelesErtek {
	
	private String rrend_szam;
	
	private Integer osszertek;
	
	private Integer engedmeny;

	public String getRrend_szam() {
		return rrend_szam;
	}

	public void setRrend_szam(String rrend_szam) {
		this.rrend_szam = rrend_szam;
	}

	public Integer getOsszertek() {
		return osszertek;
	}

	public void setOsszertek(Integer osszertek) {
		this.osszertek = osszertek;
	}

	public Integer getEngedmeny() {
		return engedmeny;
	}

	public void setEngedmeny(Integer engedmeny) {
		this.engedmeny = engedmeny;
	}

	
}
